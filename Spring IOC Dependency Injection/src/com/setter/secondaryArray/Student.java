package com.setter.secondaryArray;

public class Student {
	
	private int sid;
	private String sname;
	private MoNo[] mono;
	
	
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public MoNo[] getMono() {
		return mono;
	}
	public void setMono(MoNo[] mono) {
		this.mono = mono;
	}

}
